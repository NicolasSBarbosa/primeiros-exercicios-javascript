// Estrutura de Repetição
// for 

for (let i = 100; i > 0; i--) {
    console.log(i)
}


// Estrutura de repetição
// while

let i = 5645468468
while (i > 10) {
    console.log(i)
    i++
}

// for in

let person = {
    name: 'Nicolas',
    age: 14,
    weight: 84.3
}
for (let property in person) {
    console.log(property)
    console.log(person[property])
}